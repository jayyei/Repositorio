package com.dto;

public class AlumnoDto
{
    private Integer nroEda;
    private String apePat;
    private String desNom;
    private String desEma;

    public Integer getNroEda()
    {
        return nroEda;
    }

    public void setNroEda(Integer nroEda)
    {
        this.nroEda = nroEda;
    }

    public String getApePat()
    {
        return apePat;
    }

    public void setApePat(String apePat)
    {
        this.apePat = apePat;
    }

    public String getDesNom()
    {
        return desNom;
    }

    public void setDesNom(String desNom)
    {
        this.desNom = desNom;
    }

    public String getDesEma()
    {
        return desEma;
    }

    public void setDesEma(String desEma)
    {
        this.desEma = desEma;
    }
}